public class Numbers {
    public static void main(String[] args) {

        int num1=11;
        int num2=5;
        int sum = num1+num2;
        int multi = num1*num2;
        System.out.println("a+b = "+sum);
        System.out.println("a*b = "+multi);
        System.out.println("a/b = "+ num1/num2);
        System.out.println("a-b = "+(num1-num2));
    }
}
